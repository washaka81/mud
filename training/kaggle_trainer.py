def load_vocab():
    paths = ["training/vocab_es_en.txt", "vocab_es_en.txt"]
    if KAGGLE:
        paths = ["/kaggle/working/vocab_es_en.txt",
                 "/kaggle/input/mud-training/vocab_es_en.txt",
                 "/kaggle/input/mud-vocab/vocab_es_en.txt"] + paths
    for p in paths:
        if os.path.exists(p):
            with open(p, "r", encoding="utf-8") as f:
                return [line.strip() for line in f if line.strip()]
    return FALLBACK_VOCAB_TEXT.strip().split("\n")


def train():
    DEVICE = "cuda" if torch.cuda.is_available() else "cpu"
    print(f"[MUD Kaggle Trainer] Device: {DEVICE}")

    if DEVICE == "cuda":
        gpu_name = torch.cuda.get_device_name(0)
        print(f"  GPU: {gpu_name}")
        if "P100" in gpu_name:
            print("  P100 incompatible with this PyTorch. Falling back to CPU.")
            DEVICE = "cpu"

    if KAGGLE:
        print("[Kaggle Environment Detected]")

    if DEVICE == "cpu":
        global STEPS
        STEPS = min(STEPS, CPU_STEPS)
        print(f"  CPU mode: reducing steps to {STEPS}")

    vocab = load_vocab()
    word_to_id = {w: i for i, w in enumerate(vocab)}
    vocab_size = len(vocab)
    print(f"Vocabulary: {vocab_size} tokens")

    model = MudMoE(dim=HIDDEN, hidden_dim=FFN_HIDDEN, num_experts=EXPERTS, num_layers=NUM_LAYERS, top_k=TOP_K).to(DEVICE)
    embed = nn.Embedding(vocab_size, HIDDEN).to(DEVICE)
    nn.init.normal_(embed.weight, mean=0.0, std=0.02)

    optimizer = torch.optim.AdamW(
        list(model.parameters()) + list(embed.parameters()),
        lr=LR, weight_decay=0.01
    )
    scheduler = torch.optim.lr_scheduler.CosineAnnealingLR(optimizer, T_max=STEPS)

    corpus = CORPUS
    encoded_corpus = []
    for text in corpus:
        ids = [word_to_id.get(w, 0) for w in text.split()]
        if len(ids) >= 2:
            encoded_corpus.append(torch.tensor(ids, device=DEVICE, dtype=torch.long))

    if not encoded_corpus:
        print("ERROR: No valid training sequences. Vocab too small for corpus.")
        return

    print(f"Training {STEPS} steps on {len(encoded_corpus)} sequences...")
    model.train()
    embed.train()

    for step in tqdm(range(STEPS), desc="Training"):
        idx = step % len(encoded_corpus)
        seq = encoded_corpus[idx]
        if len(seq) < 2:
            continue
        x_ids = seq[:-1].unsqueeze(0)
        target = seq[1:]

        emb_ste = weight_quant(embed.weight)
        h = F.embedding(x_ids, emb_ste)
        h = model(h)
        if h.size(1) != target.size(0):
            target = target[:h.size(1)]
        logits = torch.matmul(h.squeeze(0), emb_ste.T)
        loss = F.cross_entropy(logits, target)
        loss_total = loss + model.balance_loss.to(DEVICE)

        optimizer.zero_grad()
        loss_total.backward()
        torch.nn.utils.clip_grad_norm_(
            list(model.parameters()) + list(embed.parameters()), 1.0)
        optimizer.step()
        scheduler.step()

        if step % 2000 == 0:
            acc = (logits.argmax(-1) == target).float().mean().item()
            lr_now = scheduler.get_last_lr()[0]
            print(f"  Step {step:5d} | Loss: {loss.item():.4f} | Acc: {acc:.3f} | Bal: {model.balance_loss.item():.4f} | LR: {lr_now:.2e}")

    out_dir = "/kaggle/working" if KAGGLE else "models"
    os.makedirs(out_dir, exist_ok=True)

    with torch.no_grad():
        emb_export = weight_quant(embed.weight)

    model_path = os.path.join(out_dir, "core_skills.mud")
    exp = MudExporter(model_path)
    exp.add_metadata("hidden_size", str(HIDDEN))
    exp.add_metadata("ffn_hidden", str(FFN_HIDDEN))
    exp.add_metadata("num_experts", str(EXPERTS))
    exp.add_metadata("num_layers", str(NUM_LAYERS))
    exp.add_metadata("arch", "mud-ternary-moe-v1")
    exp.add_metadata("tokenizer.tokens", "\n".join(vocab))
    exp.add_metadata("tokenizer.merges", "")

    sd = {
        "token_embd.weight": emb_export,
        "output_norm.weight": model.norm.weight,
    }
    for l in range(NUM_LAYERS):
        layer = model.layers[l]
        sd[f"blk.{l}.attn_q.weight"] = layer.attention.wq.weight
        sd[f"blk.{l}.attn_k.weight"] = layer.attention.wk.weight
        sd[f"blk.{l}.attn_v.weight"] = layer.attention.wv.weight
        sd[f"blk.{l}.attn_output.weight"] = layer.attention.wo.weight
        sd[f"blk.{l}.attn_norm.weight"] = layer.attention.norm.weight
        sd[f"blk.{l}.gate.weight"] = layer.gate.weight
        sd[f"blk.{l}.norm.weight"] = layer.norm.weight
        for i in range(EXPERTS):
            sd[f"blk.{l}.expert.{i}.w1.weight"] = layer.experts[i].w1.weight
            sd[f"blk.{l}.expert.{i}.w2.weight"] = layer.experts[i].w2.weight
            sd[f"blk.{l}.expert.{i}.w3.weight"] = layer.experts[i].w3.weight

    torch.save(model.state_dict(), os.path.join(out_dir, "mud_last_checkpoint.pt"))
    exp.export(sd)
    print(f"Model exported: {model_path}")
    print("MUD Kaggle training completed.")


if __name__ == "__main__":
    train()
